package de.cau.cs.kieler.synccharts.custom;

import org.eclipse.core.runtime.Plugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle.
 * 
 * @kieler.rating 2010-01-27 proposed yellow msp
 * @author msp
 */
public class SyncChartsCustomPlugin extends Plugin {

    /** the plug-in ID. */
    public static final String PLUGIN_ID = "de.cau.cs.kieler.synccharts.custom";

    /** the shared instance. */
    private static SyncChartsCustomPlugin plugin;

    /**
     * The constructor.
     */
    public SyncChartsCustomPlugin() {
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void start(final BundleContext context) throws Exception {
        super.start(context);
        plugin = this;
    }

    /**
     * {@inheritDoc}
     */
    @Override
    public void stop(final BundleContext context) throws Exception {
        plugin = null;
        super.stop(context);
    }

    /**
     * Returns the shared instance.
     * 
     * @return the shared instance
     */
    public static SyncChartsCustomPlugin getDefault() {
        return plugin;
    }

}
