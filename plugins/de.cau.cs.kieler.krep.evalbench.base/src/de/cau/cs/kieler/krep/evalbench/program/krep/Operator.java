package de.cau.cs.kieler.krep.evalbench.program.krep;

/**
 * @author ctr
 * 
 *         Arithmetic and boolean operators
 */
public enum Operator {
    /**
     * addition
     */
    ADD,

    /**
     * subtraction
     */
    SUB,

    /**
     * multiplication
     */
    MUL,

    /**
     * division
     */
    DIV,

    /**
     * boolean and
     */
    AND,

    /**
     * boolean or
     */
    OR,

    /**
     * boolean exclusive or
     */
    XOR,

    /**
     * negation
     */
    NOT;
}
